//Function to remove duplicates from sorted linked list.
Node *removeDuplicates(Node *head)
{
   Node* ptr1 = head;
   Node* ptr2 = head->next;
   Node* curr = NULL;
   while(ptr1 != NULL && ptr2 != NULL){
       if(ptr1->data == ptr2->data){
           curr = ptr2;
           ptr1->next = ptr2->next;
           ptr2 = ptr2->next;
           curr->next = NULL;
       }
      else {
          ptr1 = ptr1->next;
          ptr2 = ptr2->next;
      }
   }
   return head;
}